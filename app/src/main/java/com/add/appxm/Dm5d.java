package com.add.appxm;

public class Dm5d {
    public String rtgz;  public String sqwd;  public String hqwd; ;

    public Dm5d(String rtgz, String sqwd, String hqwd) {
        this.rtgz = rtgz;
        this.sqwd = sqwd;
        this.hqwd = hqwd;
    }

    @Override
    public String toString() {
        return "Dm5d{" +
                "rtgz='" + rtgz + '\'' +
                ", sqwd='" + sqwd + '\'' +
                ", hqwd='" + hqwd + '\'' +
                '}';
    }
}
